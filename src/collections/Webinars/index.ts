import type { CollectionConfig } from 'payload'

import {
    BlocksFeature,
    FixedToolbarFeature,
    HeadingFeature,
    HorizontalRuleFeature,
    InlineToolbarFeature,
    lexicalEditor,
} from '@payloadcms/richtext-lexical'

import { authenticated } from '../../access/authenticated'
import { authenticatedOrPublished } from '../../access/authenticatedOrPublished'
import { Banner } from '../../blocks/Banner/config'
import { Code } from '../../blocks/Code/config'
import { MediaBlock } from '../../blocks/MediaBlock/config'
import { generatePreviewPath } from '../../utilities/generatePreviewPath'

import { populateAuthors } from '../Webinars/hook/populateAuthors'
import { revalidatePost, revalidateDelete } from '../Webinars/hook/revalidatePost'

import {
    MetaDescriptionField,
    MetaImageField,
    MetaTitleField,
    OverviewField,
    PreviewField,
} from '@payloadcms/plugin-seo/fields'
import { slugField } from 'payload'

export const Webinars: CollectionConfig<'webinars'> = {
    slug: 'webinars',
    access: {
        create: authenticated,
        delete: authenticated,
        read: authenticatedOrPublished,
        update: authenticated,
    },
    defaultPopulate: {
        title: true,
        slug: true,
        categories: true,
        meta: {
            image: true,
            description: true,
        },
    },
    admin: {
        defaultColumns: ['title', 'slug', 'updatedAt'],
        livePreview: {
            url: ({ data, req }) =>
                generatePreviewPath({
                    slug: data?.slug,
                    collection: 'webinars',
                    req,
                }),
        },
        preview: (data, { req }) =>
            generatePreviewPath({
                slug: data?.slug as string,
                collection: 'webinars',
                req,
            }),
        useAsTitle: 'title',
    },
    fields: [
        {
            name: 'title',
            type: 'text',
            required: true,
        },
        {
            type: 'tabs',
            tabs: [
                {
                    fields: [
                        {
                            name: 'heroImage',
                            type: 'upload',
                            relationTo: 'media',
                        },
                        {
                            name: 'tag',
                            type: 'text',
                            required: true,
                        },
                        {
                            name: 'date',
                            type: 'text',
                            required: true,
                        },
                        {
                            name: 'Heading',
                            type: 'text',
                            required: true,
                        },
                        {
                            name: 'content',
                            type: 'richText',
                            editor: lexicalEditor({
                                features: ({ rootFeatures }) => [
                                    ...rootFeatures,
                                    HeadingFeature({ enabledHeadingSizes: ['h1', 'h2', 'h3', 'h4'] }),
                                    BlocksFeature({ blocks: [Banner, Code, MediaBlock] }),
                                    FixedToolbarFeature(),
                                    InlineToolbarFeature(),
                                    HorizontalRuleFeature(),
                                ],
                            }),
                            label: false,
                            required: true,
                        },
                        {
                            name: 'link',
                            type: 'text',
                            required: true,
                        },
                    ],
                    label: 'Content',
                },
                {
                    fields: [
                        {
                            name: 'relatedPosts',
                            type: 'relationship',
                            admin: { position: 'sidebar' },
                            filterOptions: ({ id }) => ({ id: { not_in: [id] } }),
                            hasMany: true,
                            relationTo: 'webinars',
                        },
                        {
                            name: 'categories',
                            type: 'relationship',
                            admin: { position: 'sidebar' },
                            hasMany: true,
                            relationTo: 'categories',
                        },
                    ],
                    label: 'Meta',
                }, 

                {
                    name: 'meta',
                    label: 'SEO',
                    fields: [
                        OverviewField({
                            titlePath: 'meta.title',
                            descriptionPath: 'meta.description',
                            imagePath: 'meta.image',
                        }),
                        MetaTitleField({ hasGenerateFn: true }),
                        MetaImageField({ relationTo: 'media' }),
                        MetaDescriptionField({}),
                        PreviewField({
                            hasGenerateFn: true,
                            titlePath: 'meta.title',
                            descriptionPath: 'meta.description',
                        }),
                    ],
                }, 


            ],
        },
        {
            name: 'publishedAt',
            type: 'date',
            admin: {
                date: { pickerAppearance: 'dayAndTime' },
                position: 'sidebar',
            },
            hooks: {
                beforeChange: [
                    ({ siblingData, value }) => {
                        if (siblingData._status === 'published' && !value) {
                            return new Date()
                        }
                        return value
                    },
                ],
            },
        },
        {
            name: 'authors',
            type: 'relationship',
            admin: { position: 'sidebar' },
            hasMany: true,
            relationTo: 'users',
        },
        {
            name: 'populatedAuthors',
            type: 'array',
            access: { update: () => false },
            admin: { disabled: true, readOnly: true },
            fields: [
                { name: 'id', type: 'text' },
                { name: 'name', type: 'text' },
            ],
        },
        slugField(),
    ],
    hooks: {
        afterChange: [revalidatePost],
        afterRead: [populateAuthors],
        afterDelete: [revalidateDelete],
    },
    versions: {
        drafts: {
            autosave: { interval: 100 },
            schedulePublish: true,
        },
        maxPerDoc: 50,
    },
}
